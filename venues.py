def get_venue_info(team_name: str, venue_name: str | None = None) -> dict:
    """チーム名またはスタジアム名からスタジアム情報を返す"""
    if venue_name and venue_name in J_LEAGUE_VENUES:
        return {**J_LEAGUE_VENUES[venue_name], "name": venue_name}

    home_venue = TEAM_HOME_VENUES.get(team_name, "未定")
    info = J_LEAGUE_VENUES.get(home_venue, J_LEAGUE_VENUES["未定"])
    return {**info, "name": home_venue}
